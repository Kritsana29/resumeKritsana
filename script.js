const state = {
  lang: localStorage.getItem("portfolio-language") || "en",
  theme: localStorage.getItem("portfolio-theme") || "light",
  typingTimer: null
};

const elements = {
  loader: document.getElementById("loader"),
  navToggle: document.getElementById("navToggle"),
  navMenu: document.getElementById("navMenu"),
  langToggle: document.getElementById("langToggle"),
  themeToggle: document.getElementById("themeToggle"),
  typingText: document.getElementById("typingText"),
  scrollProgress: document.getElementById("scrollProgress"),
  backToTop: document.getElementById("backToTop"),
  cursorGlow: document.getElementById("cursorGlow"),
  copyEmail: document.getElementById("copyEmail"),
  copyFeedback: document.getElementById("copyFeedback")
};

function setLanguage(lang) {
  state.lang = lang;
  localStorage.setItem("portfolio-language", lang);
  document.documentElement.lang = lang;
  elements.langToggle.textContent = lang === "en" ? "TH" : "EN";

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    node.textContent = translations[lang][key] || translations.en[key] || node.textContent;
  });

  startTyping();
}

function setTheme(theme) {
  state.theme = theme;
  document.body.classList.toggle("dark", theme === "dark");
  localStorage.setItem("portfolio-theme", theme);
  elements.themeToggle.setAttribute("aria-label", theme === "dark" ? "Switch to light mode" : "Switch to dark mode");
}

function startTyping() {
  window.clearTimeout(state.typingTimer);
  const text = translations[state.lang]["hero.title"];
  let index = 0;
  elements.typingText.textContent = "";

  const type = () => {
    elements.typingText.textContent = text.slice(0, index);
    index += 1;
    if (index <= text.length) {
      state.typingTimer = window.setTimeout(type, 34);
    }
  };

  type();
}

function updateScrollUi() {
  const scrollTop = window.scrollY;
  const maxScroll = document.documentElement.scrollHeight - window.innerHeight;
  const progress = maxScroll > 0 ? (scrollTop / maxScroll) * 100 : 0;
  elements.scrollProgress.style.width = `${progress}%`;
  elements.backToTop.classList.toggle("visible", scrollTop > 600);
}

function setActiveNav() {
  const sections = [...document.querySelectorAll("main section[id]")];
  const current = sections
    .filter((section) => section.getBoundingClientRect().top <= 140)
    .pop();

  if (!current) return;

  document.querySelectorAll(".nav-link").forEach((link) => {
    link.classList.toggle("active", link.getAttribute("href") === `#${current.id}`);
  });
}

function setupReveal() {
  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (!entry.isIntersecting) return;

      entry.target.classList.add("visible");

    });
  }, { threshold: 0.18 });

  document.querySelectorAll(".reveal").forEach((node) => observer.observe(node));
}

function setupRipple() {
  document.querySelectorAll(".ripple").forEach((button) => {
    button.addEventListener("click", (event) => {
      const rect = button.getBoundingClientRect();
      const wave = document.createElement("span");
      wave.className = "ripple-wave";
      wave.style.left = `${event.clientX - rect.left}px`;
      wave.style.top = `${event.clientY - rect.top}px`;
      button.appendChild(wave);
      window.setTimeout(() => wave.remove(), 700);
    });
  });
}

function copyEmail() {
  const email = "woqwri@gmail.com";
  const successText = translations[state.lang]["contact.copied"];

  if (navigator.clipboard) {
    navigator.clipboard.writeText(email).then(() => {
      elements.copyFeedback.textContent = successText;
      window.setTimeout(() => (elements.copyFeedback.textContent = ""), 1800);
    });
    return;
  }

  elements.copyFeedback.textContent = email;
}

window.addEventListener("load", () => {
  window.setTimeout(() => elements.loader.classList.add("hidden"), 450);
});

window.addEventListener("scroll", () => {
  updateScrollUi();
  setActiveNav();
}, { passive: true });

window.addEventListener("mousemove", (event) => {
  elements.cursorGlow.style.left = `${event.clientX}px`;
  elements.cursorGlow.style.top = `${event.clientY}px`;
}, { passive: true });

elements.navToggle.addEventListener("click", () => {
  const open = elements.navMenu.classList.toggle("open");
  elements.navToggle.setAttribute("aria-expanded", String(open));
});

document.querySelectorAll(".nav-link").forEach((link) => {
  link.addEventListener("click", () => {
    elements.navMenu.classList.remove("open");
    elements.navToggle.setAttribute("aria-expanded", "false");
  });
});

elements.langToggle.addEventListener("click", () => {
  setLanguage(state.lang === "en" ? "th" : "en");
});

elements.themeToggle.addEventListener("click", () => {
  setTheme(state.theme === "dark" ? "light" : "dark");
});

elements.backToTop.addEventListener("click", () => {
  window.scrollTo({ top: 0, behavior: "smooth" });
});

elements.copyEmail.addEventListener("click", copyEmail);

setTheme(state.theme);
setLanguage(state.lang);
setupReveal();
setupRipple();
updateScrollUi();
setActiveNav();
